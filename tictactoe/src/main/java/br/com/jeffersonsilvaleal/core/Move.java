package br.com.jeffersonsilvaleal.core;

public class Move {

	// Variável para controlar a coluna
	private int i;
	
	// Variável para controlar a linha
	private int j;

	
	// Métodos para setar(SETTERS) e retornar(GETTERS) valores
	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public int getJ() {
		return j;
	}

	public void setJ(int j) {
		this.j = j;
	}
	
	
}
